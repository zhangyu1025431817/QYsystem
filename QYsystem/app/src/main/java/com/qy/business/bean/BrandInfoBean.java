package com.qy.business.bean;

public class BrandInfoBean {

	private String brand_id;
	private String categroup_id;
	private String brand_name;
	private String brand_pic;
	private String brand_order;

	public String getBrand_id() {
		return brand_id;
	}

	public void setBrand_id(String brand_id) {
		this.brand_id = brand_id;
	}

	public String getCategroup_id() {
		return categroup_id;
	}

	public void setCategroup_id(String categroup_id) {
		this.categroup_id = categroup_id;
	}

	public String getBrand_name() {
		return brand_name;
	}

	public void setBrand_name(String brand_name) {
		this.brand_name = brand_name;
	}

	public String getBrand_pic() {
		return brand_pic;
	}

	public void setBrand_pic(String brand_pic) {
		this.brand_pic = brand_pic;
	}

	public String getBrand_order() {
		return brand_order;
	}

	public void setBrand_order(String brand_order) {
		this.brand_order = brand_order;
	}

}
